package org.firstinspires.ftc.teamcode.utils;

public class GamepadIO {
    private double leftY;
    private double leftX;
    private double rightY;
    private double rightX;

    public GamepadIO(double leftY, double leftX, double rightY, double rightX) {
        this.leftY = leftY;
        this.leftX = leftX;
        this.rightY = rightY;
        this.rightX = rightX;

    }
}